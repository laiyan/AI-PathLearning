//get the max value for this specific room or state
float max(float Q[62][62], int state) {
	float temp = 0;
	for (int i = 0; i < ACTION_NUM; i++) {
		if ((R[state][i] >= 0) && (Q[state][i] > temp)) {
			temp = Q[state][i];
		}
	}
	return temp;
}

//Start from 0, q-learning
int qlearning(int initial, float R[62][62], float Q[62][62], int flag, vector<string> r) {
	float previousQ, newQ;
	int a;
	float qmax;
	memset(adjacent_rooms, 0, 10 * sizeof(int));
	//find all the adjacent room for the initial room
	NUM = 0;
	//cout << "Room " << r.at(initial) << endl;
	for (int i = 0; i < ACTION_NUM; i++)
	{
		if (R[initial][i] >= 0)
		{
			adjacent_rooms[NUM] = i;
			NUM++;
		}
	}
	//action
	a = adjacent_rooms[rand() % NUM];
	//cout << "Action: " << a << endl;
	//update Q
	previousQ = Q[initial][a];
	//cout << "Q before: " << previousQ << endl;
	//a become now s
	qmax = max(Q, a);
	Q[initial][a] = R[initial][a] + gamma * qmax;
	newQ = Q[initial][a];
	//cout << "Q after: " << newQ << endl;

	//flag to avoid inifinite loop
	//when Q doesn't change
	if (previousQ == newQ) {
		flag++;
	}
	else {
		flag = 0;
	}

	if (flag == 800) {
		return 0;
	}

	//if reaxch goal state next episode
	if (newQ == 100) {
		initial = rand() % STATE_NUM;
	}
	else {
		//change state
		initial = a;
	}
	qlearning(initial, R, Q, flag, r);
}


//finialized q-table
int trained(float Q[62][62], int state) {
	float temp = 0;
	int final = 0;
	for (int i = 0; i < ACTION_NUM; i++) {
		if ((Q[state][i] > temp)) {
			temp = Q[state][i];
			final = i;
		}
	}
	return final;
}

